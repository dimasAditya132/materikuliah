package TugasPraktikum4;

public class Increment3 {
    public static void main(String[] args){
        int x,y,z,c,d;

        x = 4;
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);

        System.out.println("");

        y=5;
        System.out.println(y);
        System.out.println(y--);
        System.out.println(y);

        System.out.println("");

        c=++x;
        System.out.println(c);
        System.out.println(++c);
        System.out.println(c);

        System.out.println("");

        d= y--;
        System.out.println(d);
        System.out.println(--d);
        System.out.println(d);

        System.out.println("");

    }
}
