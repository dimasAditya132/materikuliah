package TugasPraktikum4;

public class Increment {
    public static void main(String args[]){
        int x = 4;

        System.out.println(x++);
        System.out.println(x);

        System.out.println(++x);
        System.out.println(x);

        System.out.println(x--);
        System.out.println(x);

        System.out.println(--x);
        System.out.println(x);

    }
}
